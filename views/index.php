    <h1><i class="fa fa-trophy" aria-hidden="true"></i> Le but du job dating? </h1>
    <p>
        Un job dating est un entretien d'embauche "express".<br />
        Durant 20 minutes, le candidat va pouvoir échanger avec un recruteur dans le but de décrocher un second entretien.<br />
        Le recruteur sera amené à rencontrer un nombre important de candidats le temps d'une journée.<br />
    </p>
</article>    
<article>
    <h1><i class="fa fa-universal-access" aria-hidden="true"></i> Les raisons de venir ?  </h1>
    <p>
        L'organisation du job dating permet de laisser une chance à tous les talents.<br />
        Vous avez la possibilité de présenter vos compétences en direct, sans passés par le filtre "CV" et "Lettre de motivation"<br />
        Cette journée est une occasion d'élargir votre réseau mais également de mieux connaître la société dans laquelle vous allez postuler.<br />
    </p>
</article>    
